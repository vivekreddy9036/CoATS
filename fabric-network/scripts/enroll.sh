#!/bin/bash
# ═══════════════════════════════════════════════════════════════════════════════
# CoATS Fabric Network — CA Enrollment & Identity Registration
# ═══════════════════════════════════════════════════════════════════════════════
#
# Enrolls CA admins, registers identities, and generates crypto material
# for the orderer and CoATS organizations using Fabric CA.
#
# This script is called by network.sh — not intended to be run standalone.
# ═══════════════════════════════════════════════════════════════════════════════

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
# shellcheck source=utils.sh
source "${SCRIPT_DIR}/utils.sh"

# ═══════════════════════════════════════════════════════════════════════════════
# NodeOUs Configuration Template
# ═══════════════════════════════════════════════════════════════════════════════

function writeNodeOUsConfig() {
  local MSP_DIR="$1"
  local CA_CERT_FILE="$2"

  cat > "${MSP_DIR}/config.yaml" <<EOF
NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/${CA_CERT_FILE}
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/${CA_CERT_FILE}
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/${CA_CERT_FILE}
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/${CA_CERT_FILE}
    OrganizationalUnitIdentifier: orderer
EOF
}

# ═══════════════════════════════════════════════════════════════════════════════
# Orderer Organization
# ═══════════════════════════════════════════════════════════════════════════════

function createOrdererOrg() {
  local CA_PORT="${CA_ORDERER_PORT:-7054}"
  local CA_NAME="ca-orderer"
  local ORG_DIR="${ORGANIZATIONS_DIR}/ordererOrg"
  local CA_DIR="${ORGANIZATIONS_DIR}/fabric-ca/ordererOrg"
  local CA_TLS_CERT="${CA_DIR}/tls-cert.pem"

  infoln "─── Creating Orderer Organization crypto material ───"

  # Wait for the CA TLS cert to exist (CA generates it on first start)
  local WAIT=0
  while [[ ! -f "${CA_TLS_CERT}" ]] && [[ $WAIT -lt 30 ]]; do
    sleep 1
    WAIT=$((WAIT + 1))
  done
  [[ -f "${CA_TLS_CERT}" ]] || fataln "Orderer CA TLS cert not found at ${CA_TLS_CERT}"

  mkdir -p "${ORG_DIR}"

  # ── 1. Enroll CA Admin ──────────────────────────────────────────────────
  infoln "Enrolling Orderer CA admin..."
  export FABRIC_CA_CLIENT_HOME="${ORG_DIR}"

  fabric-ca-client enroll \
    -u "https://${CA_ORDERER_ADMIN_USER}:${CA_ORDERER_ADMIN_PASSWORD}@localhost:${CA_PORT}" \
    --caname "${CA_NAME}" \
    --tls.certfiles "${CA_TLS_CERT}"

  # Determine the cacerts filename (auto-generated by fabric-ca-client)
  local CA_CERT_FILE
  CA_CERT_FILE="$(ls "${ORG_DIR}/msp/cacerts/" | head -1)"

  # ── 2. Write NodeOUs config to org MSP ──────────────────────────────────
  writeNodeOUsConfig "${ORG_DIR}/msp" "${CA_CERT_FILE}"

  # ── 3. Register Orderer Identity ────────────────────────────────────────
  infoln "Registering orderer identity..."
  fabric-ca-client register --caname "${CA_NAME}" \
    --id.name orderer --id.secret "${ORDERER_PASSWORD}" --id.type orderer \
    --tls.certfiles "${CA_TLS_CERT}"

  # ── 4. Register Orderer Admin Identity ──────────────────────────────────
  infoln "Registering orderer admin identity..."
  fabric-ca-client register --caname "${CA_NAME}" \
    --id.name ordererAdmin --id.secret "${ORDERER_ADMIN_PASSWORD}" --id.type admin \
    --tls.certfiles "${CA_TLS_CERT}"

  # ── 5. Enroll Orderer MSP ──────────────────────────────────────────────
  local ORDERER_DIR="${ORG_DIR}/orderers/orderer.coats.gov.in"
  infoln "Enrolling orderer MSP..."

  fabric-ca-client enroll \
    -u "https://orderer:${ORDERER_PASSWORD}@localhost:${CA_PORT}" \
    --caname "${CA_NAME}" \
    -M "${ORDERER_DIR}/msp" \
    --tls.certfiles "${CA_TLS_CERT}" \
    --csr.hosts "orderer.coats.gov.in,localhost,127.0.0.1,${EXTERNAL_HOST:-localhost}"

  cp "${ORG_DIR}/msp/config.yaml" "${ORDERER_DIR}/msp/config.yaml"

  # ── 6. Enroll Orderer TLS ──────────────────────────────────────────────
  infoln "Enrolling orderer TLS..."
  fabric-ca-client enroll \
    -u "https://orderer:${ORDERER_PASSWORD}@localhost:${CA_PORT}" \
    --caname "${CA_NAME}" \
    -M "${ORDERER_DIR}/tls" \
    --enrollment.profile tls \
    --tls.certfiles "${CA_TLS_CERT}" \
    --csr.hosts "orderer.coats.gov.in,localhost,127.0.0.1,${EXTERNAL_HOST:-localhost}"

  # Copy TLS files to standard names expected by Docker compose
  cp "${ORDERER_DIR}/tls/tlscacerts/"* "${ORDERER_DIR}/tls/ca.crt"
  cp "${ORDERER_DIR}/tls/signcerts/"*  "${ORDERER_DIR}/tls/server.crt"
  cp "${ORDERER_DIR}/tls/keystore/"*   "${ORDERER_DIR}/tls/server.key"

  # ── 7. Enroll Orderer Admin User ───────────────────────────────────────
  infoln "Enrolling orderer admin user..."
  fabric-ca-client enroll \
    -u "https://ordererAdmin:${ORDERER_ADMIN_PASSWORD}@localhost:${CA_PORT}" \
    --caname "${CA_NAME}" \
    -M "${ORG_DIR}/users/Admin@orderer.coats.gov.in/msp" \
    --tls.certfiles "${CA_TLS_CERT}"

  cp "${ORG_DIR}/msp/config.yaml" "${ORG_DIR}/users/Admin@orderer.coats.gov.in/msp/config.yaml"

  # ── 8. Build Org-level MSP ─────────────────────────────────────────────
  mkdir -p "${ORG_DIR}/msp/tlscacerts"
  cp "${ORDERER_DIR}/tls/ca.crt" "${ORG_DIR}/msp/tlscacerts/tls-ca.crt"

  successln "Orderer organization crypto material created."
}


# ═══════════════════════════════════════════════════════════════════════════════
# CoATS Organization
# ═══════════════════════════════════════════════════════════════════════════════


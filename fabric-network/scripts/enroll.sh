  cp "${PEER_DIR}/tls/ca.crt" "${ORG_DIR}/msp/tlscacerts/tls-ca.crt"

  successln "CoATS organization crypto material created."
}


# ═══════════════════════════════════════════════════════════════════════════════
# Main (when called directly)
# ═══════════════════════════════════════════════════════════════════════════════

if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
  infoln "Starting CA enrollment process..."
  createOrdererOrg
  createCoATSOrg
  successln "All crypto material generated successfully."
fi

  fabric-ca-client enroll \
    -u "https://coatsAdmin:${COATS_ADMIN_PASSWORD}@localhost:${CA_PORT}" \
    --caname "${CA_NAME}" \
    -M "${ORG_DIR}/users/Admin@coats.gov.in/msp" \
    --tls.certfiles "${CA_TLS_CERT}"

  cp "${ORG_DIR}/msp/config.yaml" "${ORG_DIR}/users/Admin@coats.gov.in/msp/config.yaml"

  # ── 9. Enroll Client (Next.js app identity) ───────────────────────────
  infoln "Enrolling CoATS client for Next.js app..."
  fabric-ca-client enroll \
    -u "https://coatsClient:${COATS_CLIENT_PASSWORD}@localhost:${CA_PORT}" \
    --caname "${CA_NAME}" \
    -M "${ORG_DIR}/users/Client@coats.gov.in/msp" \
    --tls.certfiles "${CA_TLS_CERT}"

  cp "${ORG_DIR}/msp/config.yaml" "${ORG_DIR}/users/Client@coats.gov.in/msp/config.yaml"

